namespace fastIO {
    #define BUF_SIZE 100000
    //fread -> read
    bool IOerror = 0;
    inline char nc() {
        static char buf[BUF_SIZE], *p1 = buf + BUF_SIZE, *pend = buf + BUF_SIZE;
        if(p1 == pend) {
            p1 = buf;
            pend = buf + fread(buf, 1, BUF_SIZE, stdin);
            if(pend == p1) {
                IOerror = 1;
                return -1;
            }
        }
        return *p1++;
    }
    inline bool blank(char ch) {
        return ch == ' ' || ch == '\n' || ch == '\r' || ch == '\t';
    }
    inline void read(int &x) {
        char ch;
        while(blank(ch = nc()));
        if(IOerror)
        return;
        for(x = ch - '0'; (ch = nc()) >= '0' && ch <= '9'; x = x * 10 + ch - '0');
    }
    #undef BUF_SIZE
};
int fi[MAX],inv[MAX];
int Case,n,lo;
bool ava;
void init(){
    fi[0]=1;
    for(int i=1;i<MAX;i++)fi[i]=mul(fi[i-1],i);
    inv[MAX-1]=powMM(fi[MAX-1],MOD-2,MOD);
    for(int i=MAX-2;i>=0;i--)inv[i]=mul(inv[i+1],i+1);
}
int C(int a,int b){
    return mul(fi[a],mul(inv[b],inv[a-b]));
}
struct node{
    int l,r,id;
    bool operator<(const node &z)const{
        return l!=z.l?l<z.l:r>z.r;
    }
}a[MAX];
int dfs(int l,int r){
    if(!ava)return 0;
    if(l>r)return 1;
    if(a[lo].l!=l||a[lo].r!=r){ava=0;return 0;}
    int mid=a[lo++].id;
    int re=mul(C(r-l,mid-l),dfs(l,mid-1));
    muli(re,dfs(mid+1,r));
    return re;
}
int main(){
    init();
    while(1){
        fastIO::read(n);
        if(fastIO::IOerror)break;
        for(int i=1;i<=n;i++)fastIO::read(a[i].l);
        for(int i=1;i<=n;i++)fastIO::read(a[i].r),a[i].id=i;
        sort(a+1,a+1+n);
        ava=1;lo=1;
        printf("Case #%d: %d\n", ++Case, dfs(1,n));
    }
    return 0;
}
